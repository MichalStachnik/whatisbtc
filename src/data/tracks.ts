export function getTrack(slug: string): Track | undefined {
  return TRACKS.find(t => t.slug === slug);
}

export function getLesson(trackSlug: string, lessonSlug: string) {
  const track = getTrack(trackSlug);
  if (!track) return undefined;
  for (const mod of track.modules) {
    const lesson = mod.lessons.find(l => l.slug === lessonSlug);
    if (lesson) return { lesson, module: mod, track };
  }
  return undefined;
}

export function getAllLessonsForTrack(track: Track) {
  return track.modules.flatMap(m => m.lessons);
}

export function getAdjacentLessons(track: Track, currentLessonId: string) {
  const allLessons = getAllLessonsForTrack(track);
  const idx = allLessons.findIndex(l => l.id === currentLessonId);
  return {
    prev: idx > 0 ? allLessons[idx - 1] : null,
    next: idx < allLessons.length - 1 ? allLessons[idx + 1] : null,
  };
}

export function getAdjacentTracks(trackId: string) {
  const idx = TRACKS.findIndex(t => t.id === trackId);
  return {
    prevTrack: idx > 0 ? TRACKS[idx - 1] : null,
    nextTrack: idx < TRACKS.length - 1 ? TRACKS[idx + 1] : null,
  };
}
